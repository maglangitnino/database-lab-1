USE NM_DB

CREATE TABLE CATEGORIES
	(
	CATEGORY_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	CATEGORY_NAME VARCHAR(15) NOT NULL,
	[DESCRIPTION] VARCHAR(MAX),
	PICTURE VARCHAR(30),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXCATEGORIES ON CATEGORIES([CATEGORY_NAME])

CREATE TABLE CUSTOMER_DEMOGRAPHICS
	(
	CUSTOMER_TYPE_ID CHAR(1) NOT NULL PRIMARY KEY,
	CUSTOMER_DESCRIPTION NVARCHAR(MAX),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)

CREATE TABLE CUSTOMERS
	(
	CUSTOMER_ID CHAR(20) NOT NULL PRIMARY KEY,
	COMPANY_NAME VARCHAR(40),
	CONTACT_NAME VARCHAR(30),
	CONTACT_TITLE VARCHAR(30),
	[ADDRESS] VARCHAR(60),
	CITY CHAR(15),
	REGION CHAR(15),
	POSTAL_CODE CHAR(10),
	COUNTRY CHAR(15),
	PHONE CHAR(24),
	FAX CHAR(24),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXCUSTOMERS ON CUSTOMERS(COMPANY_NAME)

CREATE TABLE CUSTOMER_CUSTOMER_DEMO
	(
	CUSTOMER_ID CHAR(20) NOT NULL,
	CUSTOMER_TYPE_ID CHAR(1) NOT NULL,
	PRIMARY KEY(CUSTOMER_ID,CUSTOMER_TYPE_ID)
	,FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID)
	,FOREIGN KEY(CUSTOMER_TYPE_ID) REFERENCES CUSTOMER_DEMOGRAPHICS(CUSTOMER_TYPE_ID)
)

CREATE TABLE EMPLOYEES
	(
	EMPLOYEE_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	LAST_NAME VARCHAR(30) NOT NULL,
	FIRST_NAME VARCHAR(20) NOT NULL,
	TITLE CHAR(30),
	TITLE_OF_COURTESY CHAR(25),
	BIRTH_DATE DATE,
	HIRE_DATE DATE,
	[ADDRESS] VARCHAR(60),
	CITY VARCHAR(15),
	REGION VARCHAR(15),
	POSTAL_CODE VARCHAR(10),
	COUNTRY VARCHAR(15),
	HOME_PHONE VARCHAR(24),
	EXTENSION VARCHAR(24),
	PHOTO VARCHAR(50),
	NOTES NVARCHAR(MAX),
	REPORTS_TO SMALLINT,
	PHOTO_PATH VARCHAR(255),
	DATE_CREATED DATETIME DEFAULT GETDATE()
	FOREIGN KEY(REPORTS_TO) REFERENCES EMPLOYEES(EMPLOYEE_ID)
)

CREATE TABLE SUPPLIERS
	(
	SUPPLIER_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	COMPANY_NAME VARCHAR(40),
	CONTACT_NAME VARCHAR(30),
	CONTACT_TITLE VARCHAR(30),
	[ADDRESS] VARCHAR(60),
	CITY CHAR(15),
	REGION CHAR(15),
	POSTAL_CODE CHAR(10),
	COUNTRY CHAR(15),
	PHONE CHAR(24),
	FAX CHAR(24),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXSUPPLIERS ON SUPPLIERS(COMPANY_NAME)

CREATE TABLE PRODUCTS
	(
	PRODUCT_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	PRODUCT_NAME VARCHAR(40) NOT NULL,
	SUPPLIER_ID SMALLINT,
	CATEGORY_ID SMALLINT,
	QUANTITY_PER_UNIT CHAR(20),
	UNIT_PRICE MONEY CHECK(UNIT_PRICE > 0),
	UNITS_IN_STOCK SMALLINT,
	UNITS_ON_ORDER SMALLINT,
	REORDER_LEVEL SMALLINT,
	DISCONTINUED INT NOT NULL,
	DATE_CREATED DATETIME DEFAULT GETDATE(),
	FOREIGN KEY(CATEGORY_ID) REFERENCES CATEGORIES(CATEGORY_ID),
	FOREIGN KEY(SUPPLIER_ID) REFERENCES SUPPLIERS(SUPPLIER_ID)
)

CREATE TABLE REGION
	(
	REGION_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	REGION_DESCRIPTION VARCHAR(100) NOT NULL,
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXREGION ON REGION(REGION_DESCRIPTION)

CREATE TABLE SHIPPERS
	(
	SHIPPER_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	COMPANY_NAME VARCHAR(40) NOT NULL,
	PHONE VARCHAR(24),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXSHIPPERS ON SHIPPERS(COMPANY_NAME)

CREATE TABLE ORDERS
	(
	ORDER_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	CUSTOMER_ID CHAR(20) NOT NULL,
	EMPLOYEE_ID SMALLINT,
	ORDER_DATE DATE,
	REQUIRED_DATE DATE,
	SHIPPED_DATE DATE,
	SHIP_VIA SMALLINT,
	FREIGHT MONEY CHECK(FREIGHT > 0),
	SHIP_NAME CHAR(40),
	SHIP_ADDRESS CHAR(60),
	SHIP_CITY CHAR(15),
	SHIP_REGION CHAR(15),
	SHIP_POSTAL_CODE CHAR(10),
	SHIP_COUNTRY CHAR(15),
	DATE_CREATED DATETIME DEFAULT GETDATE(),
	FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
	FOREIGN KEY(EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID),
	FOREIGN KEY(SHIP_VIA) REFERENCES SHIPPERS(SHIPPER_ID)
)
CREATE INDEX IDXORDERS ON ORDERS(CUSTOMER_ID,EMPLOYEE_ID,ORDER_DATE)

ALTER TABLE ORDERS
ADD CONSTRAINT CHKSHIP CHECK(SHIPPED_DATE >= ORDER_DATE)

CREATE TABLE TERRITORIES
	(
	TERRITORY_ID CHAR(20) NOT NULL PRIMARY KEY,
	TERRITORY_DESCRIPTION VARCHAR(100) NOT NULL,
	REGION_ID SMALLINT NOT NULL,
	DATE_CREATED DATETIME DEFAULT GETDATE(),
	FOREIGN KEY(REGION_ID) REFERENCES REGION(REGION_ID)
)
CREATE INDEX IDXTERRITORIES ON TERRITORIES(TERRITORY_DESCRIPTION)

CREATE TABLE EMPLOYEE_TERRITORIES
	(
	EMPLOYEE_ID SMALLINT NOT NULL,
	TERRITORY_ID CHAR(20) NOT NULL,
	PRIMARY KEY(TERRITORY_ID,EMPLOYEE_ID),
	FOREIGN KEY(TERRITORY_ID) REFERENCES TERRITORIES(TERRITORY_ID),
	FOREIGN KEY(EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID)
)

CREATE TABLE ORDER_DETAILS
	(
	ORDER_ID SMALLINT NOT NULL,
	PRODUCT_ID SMALLINT NOT NULL,
	UNIT_PRICE MONEY NOT NULL CHECK(UNIT_PRICE > 0),
	QUANTITY SMALLINT NOT NULL CHECK(QUANTITY > 0),
	DISCOUNT MONEY NOT NULL,
	DATE_CREATED DATETIME DEFAULT GETDATE(),
	PRIMARY KEY(ORDER_ID,PRODUCT_ID),
	FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID),
	FOREIGN KEY(ORDER_ID) REFERENCES ORDERS(ORDER_ID)
)

CREATE TABLE US_STATES
	(
	STATE_ID SMALLINT NOT NULL PRIMARY KEY IDENTITY(1,1),
	STATE_NAME VARCHAR(100),
	STATE_ABBR CHAR(2),
	STATE_REGION VARCHAR(50),
	DATE_CREATED DATETIME DEFAULT GETDATE()
)
CREATE INDEX IDXUS_STATES ON US_STATES(STATE_NAME)


